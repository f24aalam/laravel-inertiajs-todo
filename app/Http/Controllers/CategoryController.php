    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $categories = Category::where('user_id', Auth::user()->id)
            ->withCount('tasks')
            ->get();

        return Inertia::render('Category/Index', ['categories' => $categories]);
    }

